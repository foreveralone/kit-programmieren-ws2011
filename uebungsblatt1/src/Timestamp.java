/**
 * Represents a time and date
 */
public class Timestamp {

	/**
	 * Year part of this timestamp
	 */
	int year;
	
	/**
	 * Month part of this timestamp
	 */
	short month;
	
	/**
	 * Day part of this timestamp
	 */
	short day;
	
	/**
	 * Hours part of the time of day of this timestamp
	 */
	short hours;
	
	/**
	 *  Minutes part of the time of day of this timestamp
	 */
	short minutes;
	
}
